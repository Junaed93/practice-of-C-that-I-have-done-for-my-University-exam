#include <stdio.h>
int main(){
    for(int i=0;i<=10; i++) {
        printf("%d", i);
    }
    for(float i=0.00;i<=10.00; i++) {
        printf("\n%f", i);
    }
    for(char i='a';i<='z'; i++) {
        printf("\n%c", i);
    }
    for(int i=0;i<=100 ; i++) {
        printf("\n%d", i);
    }
   
    return 0;
} 